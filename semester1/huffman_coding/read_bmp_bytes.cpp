void solve() {
    ifstream f("photo.bmp", ios::binary);
    vector<uint8_t> v{ istreambuf_iterator<char>{f}, {} };
    vector<ll> frq(256);
    for (auto i : v)
        ++frq[i];
    cout << "Read complete, got " << v.size() << " bytes\n";
    cout << "frequency:\n";
    for (int i = 0; i < 256; ++i) {
        cout << i << ' ' << frq[i] << '\n';
    }
}

